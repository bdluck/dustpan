package io.github.bdluck.merge.basic;

/**
 * @author bdluck
 */
public enum BasicType {
    /**
     * 根据case项目转换
     */
    CASE,
    /**
     * 转为byte
     */
    BYTE,
    /**
     * 转为byte数组
     */
    BYTE_ARRAY,
    /**
     * 转short
     */
    SHORT,
    /**
     * 转int
     */
    INT,
    /**
     * 转long
     */
    LONG,
    /**
     * 转字符
     */
    STRING,
    /**
     * 转16进制字符
     */
    STRING_HEX
}
